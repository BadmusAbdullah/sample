public class ifStatement {
    public static void main(String[] args) {
        int age = 75;
        if (age >= 75) {
            System.out.println("Ok Boomer!");
        }
        else if (age >= 18) {
            System.out.println("You are an adult!");
        }
        else {
            System.out.println("You are not yet an adult!");
        }
    }
}
